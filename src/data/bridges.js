function rnd(min, max) { return +(min + Math.random() * (max - min)).toFixed(4) }
function pick(arr) { return arr[Math.floor(Math.random() * arr.length)] }
function progress() { return Math.floor(Math.random() * 70) + 15 }
function budget() {
  const v = +(Math.random() * 1500 + 500).toFixed(1)
  return { total: `₹${v} Cr`, spent: `₹${+(v * (0.35 + Math.random() * 0.5)).toFixed(1)} Cr` }
}
function year(offset = 0) {
  const y = 2026 + offset
  const m = ['Jan','Feb','Mar','Apr','May','Jun','Jul','Aug','Sep','Oct','Nov','Dec']
  return `${pick(m)} ${y}`
}

